public class Book {
    private int bookID,pages,price;
    
    public int getbookID(){
        return bookID;
    }
    
    public int getpages(){
        return pages;
    }
    
    public int getprice(){
        return price;
    }
    
    public void setbookID(int newbookID){
        this.bookID = newbookID;
    }
    
    public void setpages(int newpages){
        this.pages = newpages;
    }
    
    public void setprice(int newprice){
        this.price = newprice;
    }
}



    
